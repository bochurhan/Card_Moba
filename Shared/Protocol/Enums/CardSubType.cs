using System;

namespace CardMoba.Protocol.Enums
{
    /// <summary>
    /// 卡牌功能子类型 —— 支持多类型组合的 Flags 枚举。
    /// 
    /// 一张卡牌可以同时具有多个子类型，例如：
    /// - 「铁斩波」= 伤害 | 防御（既造成伤害，又提供护甲）
    /// - 「暗影吞噬」= 伤害（纯伤害 + 吸血触发效果）
    /// 
    /// 子类型用于：
    /// 1. UI 展示（卡牌边框、分类筛选）
    /// 2. 卡组构建规则验证
    /// 3. 某些效果的类型检查（如"反制首张伤害牌"）
    /// 
    /// 注意：实际结算层由 EffectType 决定，而非 SubType。
    /// </summary>
    [Flags]
    public enum CardSubType
    {
        /// <summary>未定义/默认</summary>
        无 = 0,

        /// <summary>伤害型：对目标造成数值伤害（堆叠2层-步骤1）</summary>
        伤害 = 1 << 0,      // 1

        /// <summary>防御型：护甲、护盾、伤害减免、无敌、免伤（堆叠1层）</summary>
        防御 = 1 << 1,      // 2

        /// <summary>反制型：无效化/惩罚对手卡牌（本回合锁定，下回合堆叠0层触发）</summary>
        反制 = 1 << 2,      // 4

        /// <summary>增益型：攻击/力量增加、伤害加深等正面数值修正（堆叠1层或堆叠3层）</summary>
        增益 = 1 << 3,      // 8

        /// <summary>减益型：攻击降低、易伤、破甲等负面数值修正（堆叠1层或堆叠3层）</summary>
        减益 = 1 << 4,      // 16

        /// <summary>支援型：临时跨路移动、队友属性加成等（堆叠3层）</summary>
        支援 = 1 << 5,      // 32

        /// <summary>传说特殊型：独立优先级，专属全屏动画（堆叠3层传说专属子阶段）</summary>
        传说特殊 = 1 << 6,  // 64

        /// <summary>控制型：沉默、眩晕、减速等（堆叠3层）</summary>
        控制 = 1 << 7,      // 128

        /// <summary>资源型：抽牌、回能、手牌保留等（堆叠3层）</summary>
        资源 = 1 << 8,      // 256
    }
}